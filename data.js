// –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
function updateTimestamp() {
    const now = new Date();
    const hours = now.getHours().toString().padStart(2, '0');
    const minutes = now.getMinutes().toString().padStart(2, '0');
    const seconds = now.getSeconds().toString().padStart(2, '0');
    
    const updateElement = document.getElementById('update-date');
    if (updateElement) {
        updateElement.textContent = `${now.toLocaleDateString('ru-RU')} ${hours}:${minutes}:${seconds}`;
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –≤—Ä–µ–º—è —Å –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
    const lastUpdate = new Date(visualizationData.lastUpdate);
    const diffSeconds = Math.floor((now - lastUpdate) / 1000);
    
    const updateTimeElement = document.getElementById('update-time');
    if (updateTimeElement) {
        updateTimeElement.textContent = diffSeconds;
    }
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
function updateStats() {
    const totalRulersElement = document.getElementById('total-rulers');
    const activeRulersElement = document.getElementById('active-rulers');
    const totalConnectionsElement = document.getElementById('total-connections');
    
    if (totalRulersElement) {
        totalRulersElement.textContent = visualizationData.nodes.length;
    }
    
    if (totalConnectionsElement) {
        totalConnectionsElement.textContent = visualizationData.links.length;
    }
    
    // –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∞–∫—Ç–∏–≤–Ω—ã—Ö
    const activeCount = visualizationData.nodes.filter(node => 
        node.status === 'active' || node.status === 'admin'
    ).length;
    
    if (activeRulersElement) {
        activeRulersElement.textContent = activeCount;
    }
}

